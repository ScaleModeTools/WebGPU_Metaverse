function groupAvatar124AnimationsByKey<TKey extends string>(
  resolveKey: (animation: Avatar124Animation) => TKey
): Readonly<Record<TKey, readonly Avatar124Animation[]>> {
  const groups = new Map<TKey, Avatar124Animation[]>();

  for (const animation of AVATAR_124_ANIMATION_CATALOG) {
    const key = resolveKey(animation);
    const group = groups.get(key);

    if (group === undefined) {
      groups.set(key, [animation]);
      continue;
    }

    group.push(animation);
  }

  return Object.freeze(
    Object.fromEntries(
      [...groups.entries()].map(([key, animations]) => [
        key,
        Object.freeze(animations)
      ])
    )
  ) as Readonly<Record<TKey, readonly Avatar124Animation[]>>;
}

function countAvatar124AnimationsByKey<TKey extends string>(
  groups: Readonly<Record<TKey, readonly Avatar124Animation[]>>
): Readonly<Record<TKey, number>> {
  const counts = (Object.keys(groups) as TKey[]).map((key) => [
    key,
    groups[key].length
  ]);

  return Object.freeze(
    Object.fromEntries(counts)
  ) as Readonly<Record<TKey, number>>;
}

function mapAvatar124AnimationIdsByKey<TKey extends string>(
  groups: Readonly<Record<TKey, readonly Avatar124Animation[]>>
): Readonly<Record<TKey, readonly Avatar124AnimationId[]>> {
  const idsByKey = (Object.keys(groups) as TKey[]).map((key) => [
    key,
    Object.freeze(groups[key].map((animation) => animation.id))
  ]);

  return Object.freeze(
    Object.fromEntries(idsByKey)
  ) as Readonly<Record<TKey, readonly Avatar124AnimationId[]>>;
}

